struct OrderListView: View {
    
    let orders : [OrderViewModel]
    
    init(orders: [OrderViewModel]) {
        self.orders = orders
    }
    
    var body: some View {
        List {
            ForEach(self.orders, id: \.id) { order in
                OrderCellView(order: order)
            }
        }
    }
}

